func TestGenerateAdapterBasic(t *testing.T) {
	// 创建临时目录
	tmpDir := t.TempDir()

	// 创建测试输入文件
	inputContent := `package testpkg

type Book struct {
	Title  string  ` + "`" + `item:"title,required"` + "`" + `
	Author string  ` + "`" + `item:"author,default=Unknown"` + "`" + `
	Price  float64 ` + "`" + `item:"price"` + "`" + `
	ISBN   string  ` + "`" + `json:"isbn"` + "`" + `
}
`
	inputFile := filepath.Join(tmpDir, "items.go")
	if err := os.WriteFile(inputFile, []byte(inputContent), 0644); err != nil {
		t.Fatalf("failed to write input file: %v", err)
	}

	// 运行生成器
	outputFile := filepath.Join(tmpDir, "book_adapter_gen.go")
	err := runGenerateAdapter([]string{
		"-type=Book",
		"-input=" + inputFile,
		"-output=" + filepath.Base(outputFile),
	})
	if err != nil {
		t.Fatalf("generate-adapter failed: %v", err)
	}

	// 验证输出文件存在
	content, err := os.ReadFile(outputFile)
	if err != nil {
		t.Fatalf("failed to read output file: %v", err)
	}

	code := string(content)

	// 验证生成的代码包含关键内容
	checks := []string{
		"Code generated by scrapy-go generate-adapter; DO NOT EDIT",
		"package testpkg",
		"type BookAdapter struct",
		"func NewBookAdapter",
		"func (a *BookAdapter) Item() any",
		"func (a *BookAdapter) FieldNames() []string",
		"func (a *BookAdapter) GetField(name string) (any, bool)",
		"func (a *BookAdapter) SetField(name string, value any) error",
		"func (a *BookAdapter) HasField(name string) bool",
		"func (a *BookAdapter) AsMap() map[string]any",
		"func (a *BookAdapter) Len() int",
		"func (a *BookAdapter) FieldMeta(name string) item.FieldMeta",
		`"title"`,
		`"author"`,
		`"price"`,
		`"isbn"`,
		`"required"`,
		`"default"`,
		"var _ item.ItemAdapter",
	}

	for _, check := range checks {
		if !strings.Contains(code, check) {
			t.Errorf("generated code missing: %q", check)
		}
	}
}

