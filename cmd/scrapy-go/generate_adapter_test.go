func TestParseItemTagFromRaw(t *testing.T) {
	tests := []struct {
		rawTag    string
		name      string
		required  bool
		defVal    string
		omitempty bool
	}{
		{`item:"title,required"`, "title", true, "", false},
		{`item:"author,default=Unknown"`, "author", false, "Unknown", false},
		{`item:"host,required,default=localhost"`, "host", true, "localhost", false},
		{`json:"isbn"`, "isbn", false, "", false},
		{`item:"field,omitempty"`, "field", false, "", true},
		{`item:"name" json:"name"`, "name", false, "", false},
	}

	for _, tt := range tests {
		name, required, defVal, omitempty, _ := parseItemTagFromRaw(tt.rawTag)
		if name != tt.name {
			t.Errorf("tag=%q: name=%q, want %q", tt.rawTag, name, tt.name)
		}
		if required != tt.required {
			t.Errorf("tag=%q: required=%v, want %v", tt.rawTag, required, tt.required)
		}
		if defVal != tt.defVal {
			t.Errorf("tag=%q: default=%q, want %q", tt.rawTag, defVal, tt.defVal)
		}
		if omitempty != tt.omitempty {
			t.Errorf("tag=%q: omitempty=%v, want %v", tt.rawTag, omitempty, tt.omitempty)
		}
	}
}

func TestExtractTagValue(t *testing.T) {
	tests := []struct {
		rawTag string
		key    string
		want   string
	}{
		{`item:"title,required"`, "item", "title,required"},
		{`json:"name" item:"title"`, "item", "title"},
		{`json:"name,omitempty"`, "json", "name,omitempty"},
		{`json:"name"`, "item", ""},
	}

	for _, tt := range tests {
		got := extractTagValue(tt.rawTag, tt.key)
		if got != tt.want {
			t.Errorf("extractTagValue(%q, %q) = %q, want %q", tt.rawTag, tt.key, got, tt.want)
		}
	}
}
