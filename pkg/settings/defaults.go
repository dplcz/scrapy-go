package settings

import "net/http"

// loadDefaults 加载所有默认配置值。
// 对应 Scrapy 的 scrapy/settings/default_settings.py。
func (s *Settings) loadDefaults() {
	d := PriorityDefault

	// ========================================================================
	// 基础配置
	// ========================================================================

	s.Set("BOT_NAME", "scrapybot", d)
	s.Set("USER_AGENT", "scrapy-go/0.3.0 (+https://github.com/example/scrapy-go)", d)

	// ========================================================================
	// 并发控制
	// ========================================================================

	s.Set("CONCURRENT_REQUESTS", 16, d)
	s.Set("CONCURRENT_REQUESTS_PER_DOMAIN", 8, d)
	s.Set("CONCURRENT_ITEMS", 100, d)

	// ========================================================================
	// 下载配置
	// ========================================================================

	s.Set("DOWNLOAD_DELAY", 0, d)                  // 下载延迟（秒），0 表示无延迟
	s.Set("DOWNLOAD_TIMEOUT", 180, d)               // 下载超时（秒）
	s.Set("DOWNLOAD_MAXSIZE", 1024*1024*1024, d)    // 最大下载大小（1GB）
	s.Set("DOWNLOAD_WARNSIZE", 32*1024*1024, d)     // 下载大小警告阈值（32MB）
	s.Set("DOWNLOAD_FAIL_ON_DATALOSS", true, d)     // 数据丢失时是否失败
	s.Set("RANDOMIZE_DOWNLOAD_DELAY", true, d)      // 是否随机化下载延迟

	// ========================================================================
	// 默认请求头
	// ========================================================================

	s.Set("DEFAULT_REQUEST_HEADERS", http.Header{
		"Accept":          {"text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8"},
		"Accept-Language": {"en"},
	}, d)

	// ========================================================================
	// 重试配置
	// ========================================================================

	s.Set("RETRY_ENABLED", true, d)
	s.Set("RETRY_TIMES", 2, d)                     // 初始请求 + 2 次重试 = 3 次请求
	s.Set("RETRY_HTTP_CODES", []int{500, 502, 503, 504, 522, 524, 408, 429}, d)
	s.Set("RETRY_PRIORITY_ADJUST", -1, d)           // 重试请求优先级调整

	// ========================================================================
	// 重定向配置
	// ========================================================================

	s.Set("REDIRECT_ENABLED", true, d)
	s.Set("REDIRECT_MAX_TIMES", 20, d)              // 最大重定向次数
	s.Set("REDIRECT_PRIORITY_ADJUST", 2, d)         // 重定向请求优先级调整

	// ========================================================================
	// 深度控制
	// ========================================================================

	s.Set("DEPTH_LIMIT", 0, d)                      // 爬取深度限制，0 表示无限制
	s.Set("DEPTH_PRIORITY", 0, d)
	s.Set("DEPTH_STATS_VERBOSE", false, d)

	// ========================================================================
	// URL 长度限制
	// ========================================================================

	s.Set("URLLENGTH_LIMIT", 2083, d)

	// ========================================================================
	// Cookies 配置
	// ========================================================================

	s.Set("COOKIES_ENABLED", true, d)
	s.Set("COOKIES_DEBUG", false, d)

	// ========================================================================
	// HTTP 认证
	// ========================================================================

	s.Set("HTTP_USER", "", d)
	s.Set("HTTP_PASS", "", d)
	s.Set("HTTP_AUTH_DOMAIN", "", d)

	// ========================================================================
	// HTTP 压缩
	// ========================================================================

	s.Set("COMPRESSION_ENABLED", true, d)

	// ========================================================================
	// HTTP 代理
	// ========================================================================

	s.Set("HTTPPROXY_ENABLED", true, d)
	s.Set("HTTPPROXY_AUTH_ENCODING", "latin-1", d)

	// ========================================================================
	// HTTP 缓存
	// ========================================================================

	s.Set("HTTPCACHE_ENABLED", false, d)
	s.Set("HTTPCACHE_DIR", "httpcache", d)
	s.Set("HTTPCACHE_EXPIRATION_SECS", 0, d)
	s.Set("HTTPCACHE_GZIP", false, d)
	s.Set("HTTPCACHE_IGNORE_HTTP_CODES", []int{}, d)

	// ========================================================================
	// Robots.txt
	// ========================================================================

	s.Set("ROBOTSTXT_OBEY", false, d)

	// ========================================================================
	// 调度器配置
	// ========================================================================

	s.Set("SCHEDULER_DEBUG", false, d)
	s.Set("JOBDIR", "", d)

	// ========================================================================
	// Scraper 配置
	// ========================================================================

	s.Set("SCRAPER_SLOT_MAX_ACTIVE_SIZE", 5000000, d) // 5MB

	// ========================================================================
	// 日志配置
	// ========================================================================

	s.Set("LOG_ENABLED", true, d)
	s.Set("LOG_LEVEL", "DEBUG", d)
	s.Set("LOG_FILE", "", d)

	// ========================================================================
	// 统计配置
	// ========================================================================

	s.Set("STATS_DUMP", true, d)
	s.Set("LOGSTATS_INTERVAL", 60.0, d)

	// ========================================================================
	// 关闭条件
	// ========================================================================

	s.Set("CLOSESPIDER_ERRORCOUNT", 0, d)
	s.Set("CLOSESPIDER_ITEMCOUNT", 0, d)
	s.Set("CLOSESPIDER_PAGECOUNT", 0, d)
	s.Set("CLOSESPIDER_TIMEOUT", 0, d)

	// ========================================================================
	// 内存监控
	// ========================================================================

	s.Set("MEMUSAGE_ENABLED", true, d)
	s.Set("MEMUSAGE_CHECK_INTERVAL_SECONDS", 60.0, d)
	s.Set("MEMUSAGE_LIMIT_MB", 0, d)
	s.Set("MEMUSAGE_WARNING_MB", 0, d)

	// ========================================================================
	// 下载器中间件（优先级字典）
	// ========================================================================

	s.Set("DOWNLOADER_MIDDLEWARES_BASE", map[string]int{
		"DownloadTimeout":  300,
		"DefaultHeaders":   400,
		"HttpAuth":         410,
		"UserAgent":        500,
		"Retry":            550,
		"HttpCompression":  590,
		"Redirect":         600,
		"Cookies":          700,
		"HttpProxy":        750,
		"DownloaderStats":  850,
	}, d)
	s.Set("DOWNLOADER_MIDDLEWARES", map[string]int{}, d)

	// ========================================================================
	// Spider 中间件（优先级字典）
	// ========================================================================

	s.Set("SPIDER_MIDDLEWARES_BASE", map[string]int{
		"HttpError": 50,
		"Offsite":   500,
		"Referer":   700,
		"UrlLength": 800,
		"Depth":     900,
	}, d)
	s.Set("SPIDER_MIDDLEWARES", map[string]int{}, d)

	// ========================================================================
	// Item Pipeline（优先级字典）
	// ========================================================================

	s.Set("ITEM_PIPELINES_BASE", map[string]int{}, d)
	s.Set("ITEM_PIPELINES", map[string]int{}, d)

	// ========================================================================
	// 扩展（优先级字典）
	// ========================================================================

	s.Set("EXTENSIONS_BASE", map[string]int{
		"CoreStats":   0,
		"CloseSpider": 0,
		"LogStats":    0,
		"MemoryUsage": 0,
		"FeedExport":  0,
	}, d)
	s.Set("EXTENSIONS", map[string]int{}, d)

	// ========================================================================
	// 下载处理器
	// ========================================================================

	s.Set("DOWNLOAD_HANDLERS_BASE", map[string]string{
		"http":  "HTTPDownloadHandler",
		"https": "HTTPDownloadHandler",
	}, d)
	s.Set("DOWNLOAD_HANDLERS", map[string]string{}, d)

	// ========================================================================
	// 去重过滤器
	// ========================================================================

	s.Set("DUPEFILTER_DEBUG", false, d)

	// ========================================================================
	// 下载器统计
	// ========================================================================

	s.Set("DOWNLOADER_STATS", true, d)

	// ========================================================================
	// DNS 配置
	// ========================================================================

	s.Set("DNSCACHE_ENABLED", true, d)
	s.Set("DNSCACHE_SIZE", 10000, d)
	s.Set("DNS_TIMEOUT", 60, d)

	// ========================================================================
	// 数据导出（Feed Export）
	// ========================================================================

	s.Set("FEED_EXPORT_ENCODING", "", d)
	s.Set("FEED_EXPORT_INDENT", 0, d)
	s.Set("FEED_STORE_EMPTY", true, d)
	s.Set("FEED_EXPORT_BATCH_ITEM_COUNT", 0, d)
	// FEEDS: map[string]map[string]any 形式的多目标导出配置
	// 示例：
	//   s.Set("FEEDS", map[string]map[string]any{
	//       "output.json": {"format": "json", "overwrite": true},
	//       "output.csv":  {"format": "csv"},
	//   }, settings.PriorityProject)
	s.Set("FEEDS", map[string]map[string]any{}, d)
	// 向后兼容字段（Scrapy 旧式单文件输出）
	s.Set("FEED_URI", "", d)
	s.Set("FEED_FORMAT", "", d)

	// ========================================================================
	// HTTP 错误过滤
	// ========================================================================

	s.Set("HTTPERROR_ALLOW_ALL", false, d)
	s.Set("HTTPERROR_ALLOWED_CODES", []int{}, d)

	// ========================================================================
	// Referer 配置
	// ========================================================================

	s.Set("REFERER_ENABLED", true, d)

	// ========================================================================
	// Offsite 配置
	// ========================================================================

	s.Set("METAREFRESH_ENABLED", true, d)
	s.Set("METAREFRESH_MAXDELAY", 100, d)

	// ========================================================================
	// CrawlSpider 配置
	// ========================================================================

	s.Set("CRAWLSPIDER_FOLLOW_LINKS", true, d)
}
